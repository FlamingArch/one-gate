class SignUpHandler {
  name = ""
  userName = ""
  password = ""
  phoneNo = ""
  email = ""
  apartmentsName = ""
  houseNo = ""

  constructor() { }

  set setName(value: string) { this.name = value }
  set setUserName(value: string) { this.userName = value }
  set setPassword(value: string) { this.password = value }
  set setPhoneNo(value: string) { this.phoneNo = value }
  set setEmail(value: string) { this.email = value }
  set setApartmentsName(value: string) { this.apartmentsName = value }
  set setHouseNo(value: string) { this.houseNo = value }
}
